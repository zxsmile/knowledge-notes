function fn(arr,target){
    if(arr.length===0){
        return -1
    }
    let left = 0
    let right = arr.length-1
    while(left<=right){
        let mid = Math.floor((left+right)/2)
        if(arr[mid]===target){
            return mid
        }
        if(arr[mid]<target){
            left = mid+1
        }else if(arr[mid]>target){
            right = mid-1
        }
    }

    return -1
}